export function teacherT(lang, key) {
  const dict = TEACHER_I18N[lang] ?? TEACHER_I18N.en;
  const fallback = TEACHER_I18N.en;
  return dict[key] ?? fallback[key] ?? key;
}

/** Localize stored English weight summary lines (e.g. "error 60%, time 30%, click 10%"). */
export function translateWeightsLine(line, lang) {
  if (!line || typeof line !== 'string') return line;
  const t = (k) => teacherT(lang, k);
  return line
    .replace(/\berror\b/gi, t('w_error'))
    .replace(/\btime\b/gi, t('w_time'))
    .replace(/\bclick\b/gi, t('w_click'))
    .replace(/\bdrag\b/gi, t('w_drag'));
}

export function currentTeacherLang() {
  const el = document.querySelector('#translateLang');
  return el?.value ?? 'en';
}
